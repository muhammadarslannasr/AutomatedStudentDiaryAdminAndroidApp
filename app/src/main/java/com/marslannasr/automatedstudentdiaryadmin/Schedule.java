public class Schedule {

    public String postTitleEt;
    public String imageButton;
    public String timestamp;
    public String schedule_ID;

    public String getPostTitleEt() {
        return postTitleEt;
    }

    public void setPostTitleEt(String postTitleEt) {
        this.postTitleEt = postTitleEt;
    }

    public String getImageButton() {
        return imageButton;
    }

    public void setImageButton(String imageButton) {
        this.imageButton = imageButton;
    }

    public String getTimestamp() {
        return timestamp;
    }

    public void setTimestamp(String timestamp) {
        this.timestamp = timestamp;
    }

    public String getSchedule_ID() {
        return schedule_ID;
    }

    public void setSchedule_ID(String schedule_ID) {
        this.schedule_ID = schedule_ID;
    }
}
